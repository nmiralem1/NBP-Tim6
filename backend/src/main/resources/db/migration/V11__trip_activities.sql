CREATE TABLE NBPT6.TRIP_ACTIVITIES (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TRIP_ID NUMBER NOT NULL,
    ACTIVITY_ID NUMBER NOT NULL,
    ADDED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT FK_TRIP_ACT_TRIP FOREIGN KEY (TRIP_ID) REFERENCES NBPT6.TRIPS(ID),
    CONSTRAINT FK_TRIP_ACT_ACT FOREIGN KEY (ACTIVITY_ID) REFERENCES NBPT6.ACTIVITIES(ID)
);
